require "rails_helper"

RSpec.feature "user visits subscriptions path" do
  scenario "and sees their subscriptions" do
    user = create(:user)
    subscriber = create(:subscriber, email: user.email)
    subscription = create(:subscription, subscriber: subscriber)

    visit subscription_url(token: subscription.token)

    expect(page).to have_text("You are currently Subscribed to ALL emails.")
  end
end